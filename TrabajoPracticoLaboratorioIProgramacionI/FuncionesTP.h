#ifndef FUNCIONESTP_H_INCLUDED
#define FUNCIONESTP_H_INCLUDED


/** \brief suma dos numeros flotantes entre si
 *
 * \param el primer numero flotante a ser sumado
 * \param el segundo numero flotante a ser sumado
 * \return el resultado de la suma
 */
float sumar (float num1, float num2);


/** \brief resta dos numeros flotantes entre si
 *
 * \param el primer numero flotante a ser restado
 * \param el segundo numero flotante a ser restado
 * \return el resultado de la resta
 */
float restar (float num1, float num2);


/** \brief multiplica dos numeros flotantes entre si
 *
 * \param el primer numero flotante a ser multiplicado
 * \param el segundo numero flotante a ser multiplicado
 * \return el resultado de la multiplicacion
 */
float multiplicar (float num1, float num2);


/** \brief divide dos numeros flotantes entre si
 *
 * \param el primer numero flotante a ser dividido
 * \param el el cantidad de veces por las que se va a dividir el numero
 * \return el resultado de la division
 */
float dividir (float num1, float num2);


/** \brief toma un numero y desarrolla su factorial
 *
 * \param el numero base del factorial
 * \return el factorial del numero elegido
 */
long int factoriar (int num1);

#endif
